"use client";

import { useRef, useState } from "react";
import { useAppState } from "@/components/app/AppState";
import { UploadEmptyState } from "@/components/home/UploadEmptyState";
import { Processing } from "@/components/home/Processing";
import { Results } from "@/components/home/Results";
import { DEFAULT_ANALYSIS } from "@/lib/analysis/default";
import type { Analysis } from "@/lib/analysis/types";

/**
 * /home is a small state machine:
 *   not analyzed  → UploadEmptyState (no bottom nav)
 *   processing    → Processing (waits on the real fetch, never fades blank)
 *   analyzed      → Results (bottom nav appears)
 *
 * Every onboarding path hands over a fetcher that resolves to an Analysis.
 * If the backend is unreachable the app falls back to the built-in demo
 * analysis (same shape, generated by the same engine) — the product never
 * dead-ends, but Results is told so it can say so honestly. Returning users
 * land straight on Results from localStorage.
 */
export default function HomePage() {
  const { analyzed, hydrated, setAnalyzed, setAnalysis } = useAppState();
  const [processing, setProcessing] = useState(false);
  const resultRef = useRef<Analysis | null>(null);
  const isDemoRef = useRef(false);
  const pendingRef = useRef<Promise<void> | null>(null);

  const start = (fetcher: () => Promise<Analysis>) => {
    resultRef.current = null;
    isDemoRef.current = false;
    pendingRef.current = fetcher()
      .then((a) => {
        resultRef.current = a;
      })
      .catch(() => {
        resultRef.current = DEFAULT_ANALYSIS; // backend down → demo mode
        isDemoRef.current = true;
      });
    setProcessing(true);
  };

  const finish = () => {
    setAnalysis(resultRef.current ?? DEFAULT_ANALYSIS, isDemoRef.current);
    setProcessing(false);
    setAnalyzed(true);
  };

  // avoid an empty→results flash before localStorage is read
  if (!hydrated) {
    return (
      <div className="fixed inset-0 z-40 flex items-center justify-center bg-base">
        <span className="text-[20px] font-extrabold text-gold">Ants</span>
      </div>
    );
  }

  if (processing) {
    return <Processing onDone={finish} waitFor={pendingRef.current ?? undefined} />;
  }

  if (analyzed) {
    return <Results />;
  }

  return <UploadEmptyState onStart={start} />;
}
